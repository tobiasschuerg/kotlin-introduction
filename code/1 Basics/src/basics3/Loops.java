package basics3;

/**
 * Java <-> Kotlin conversion
 * Loops
 */
public class Loops {

    public static void main(String... args) {

        System.out.println("Hello World");

        for (int i = 0; i < 10; i++) {
            int square = i * i;
            System.out.println("The square of " + i + " is " + square);
        }

    }

}
