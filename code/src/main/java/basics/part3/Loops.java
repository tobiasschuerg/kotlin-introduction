package basics.part3;

/**
 * Java <-> Kotlin conversion
 * Loops
 */
public class Loops {

    public static void main(String... args) {

        for (int number = 0; number < 10; number++) {
            int square = number * number;
            System.out.println("The square of " + number + " is " + square);
        }

    }

}
